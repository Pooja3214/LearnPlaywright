let age="Thirty";//This is string literal assigned to variable age
let isStudent=true;//This is boolean literal assigned to variable isStudent
let name='pooja';//This is string literal assigned to variable name
let pi=3.14;//This is numeric literal assigned to variable pi
let nullValue=null;//This is null literal assigned to variable nullValue
let undefined;//This variable is declared but not assigned, so it has the value undefined


//typeof operator can be used to check the type of a literal
console.log(typeof age); // Output: string
console.log(typeof isStudent); // Output: boolean
console.log(typeof name); // Output: string         
console.log(typeof pi); // Output: number
console.log(typeof nullValue); // Output: object (this is a quirk in JavaScript, null is considered an object)
console.log(typeof undefined); // Output: undefined